// Helper: get stored language preference
export function getLang() {
  if (typeof window === "undefined") return "en";
  return localStorage.getItem("fixnext_lang") || "en";
}

// Helper: set language preference  
export function setLang(lang) {
  if (typeof window !== "undefined") {
    localStorage.setItem("fixnext_lang", lang);
  }
}